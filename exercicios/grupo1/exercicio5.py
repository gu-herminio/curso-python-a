#Calcule a área de um quadrado. Como calcular: Area = lado * lado

#Início
print(f"=============================Calculadora de área=============================")
lado = float(input("Digite o valor em centimetros dos lados do quadrado: "))

#Cálculo
area = lado ** 2

#Resultado final
print(f"A área do quadrado cujo os lados possuem {lado:.2f}cm é {area:.2f}cm².")


